                    جزو نمایندگان یا کارکنان خود نیست را ندارد مگر در مواردی که رسما و صراحتا به صورت مکتوب از سوی شایان مجاز شناخته شده باشند. مشتری تنها مجاز به استفاده از خدمات شایان در راستای اهداف قانونی و مطابق با این
