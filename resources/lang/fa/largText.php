                    <strong>2-</strong>&nbsp;انطباق محتوای منتشر شده با تمامی قوانین کپی رایت، انتشار و توزیع محتوا در جمهوری اسلامی ایران<br />
                    <strong>3-</strong>&nbsp;تهیه پشتیان از محتوای خود جهت ارایه به مراجع قانونی و قضایی در صورت وجود هرگونه شکایت و یا دستور قضایی
