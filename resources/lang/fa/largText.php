        شایان است که می‌توانند به شهرت شایان و خدمات آن لطمه وارد نمایند و در بهره‌برداری از خدمات شایان ممنوع هستند.<br />
        راهنمایی‌ها و ممنوعیت‌های بیان شده در این توافق‌نامه در استفاده از خدمات شایان توسط مشتری می‌بایست به همان اندازه توسط کارکنان مشتری و هر شخص حقیقی و حقوقی دیگری که به‌صورت مستقیم یا غیرستقیم و به واسطه مشتری به خدمات شایان
        دسترسی پیدا نموده اند اعمال گردد. استفاده از خدمات شایان به منزله مطالعه، آگاهی و پذیرش مشتری از تمامی مفاد این توافق‌نامه است و مشتری متعهد می‌گردد که کاربران و مخاطبین خود (مشتری) مطابق با مفاد این توافق‌نامه عمل خواهند نمود و
        در صورت نقض مفاد این توافق‌نامه توسط مشتری یا کاربران ایشان مسئولیت کامل عواقب ناشی از آن بر عهده مشتری است.
