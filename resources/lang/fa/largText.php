                    غیرقانونی، توهین آمیز، افترا آمیز، زشت و ناپسند، غیراخلاقی (پورن)، ضد دینی، نژادپرستانه و در تضاد با شرایط عمومی پذیرفته شده توسط جامعه اینترنتی باشد، استفاده نماید. مشتری موظف است اطمینان حاصل نماید که محتوای خود و
